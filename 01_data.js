function shuffleArray(arr) {
  let a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// 각 서가에서 4문장 중 3개를 뽑고, 각 문장에 옳/틀린 버전을 랜덤 배정
function buildBossQuiz(stageIdx) {
  let correct = CORRECT_SENTENCES[stageIdx];
  let wrong   = WRONG_SENTENCES[stageIdx];
  let idxs = [0, 1, 2, 3];
  for (let i = idxs.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [idxs[i], idxs[j]] = [idxs[j], idxs[i]];
  }
  let chosen = idxs.slice(0, 3);
  let quiz = chosen.map(i => {
    let useCorrect = Math.random() < 0.5;
    return { quiz: useCorrect ? correct[i] : wrong[i], ans: useCorrect ? 'O' : 'X' };
  });
  for (let i = quiz.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [quiz[i], quiz[j]] = [quiz[j], quiz[i]];
  }
  return quiz;
}

